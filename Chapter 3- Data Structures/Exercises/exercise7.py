places = [ "Japan", "Switzerland", "Indonesia", "Thailand", "New Zealand"]
print("Places I'd like to visit: \n", places)
print("\n")
places.reverse()
print("Places I'd like to visit(reverse): \n", places)
print("\n")
places.reverse()
print("Places I'd like to visit(back-to-the-original): \n", places)
print("\n")

